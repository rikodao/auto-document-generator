## ファイル概要

このスクリプトは、AWSアカウントにRfl-Prodスタックを直接デプロイするために必要なすべてのリソースを準備するためのデプロイメントスクリプトです。
関連するモジュール: aws cli、aws cdk、nodejs、python、その他のツール類

## 主要なサブルーチン

- color_red/green/reset: コンソール出力の色を変更する
- is_present: 指定されたコマンドがインストールされているかどうかを確認する
  - 引数: コマンド名
  - 戻り値: 0(インストールされている)、1(インストールされていない)
  - グローバル変数: なし

## データ構造

特に明示的なデータ構造はありません。
スクリプト内で使用される変数はグローバル変数です。

## 主要なアルゴリズム

- CDKアプリケーションをシンセサイズしてデプロイ用テンプレートを生成
- デプロイ用アセットを圧縮
- awsクレデンシャルの設定と確認
- CDKブートストラップの実行
- デプロイの実行

## 入出力

- 入力: RFL_STACK_NAME、S3_REGION環境変数
- 出力: デプロイ用テンプレートファイル、圧縮されたデプロイアセット
- データベースアクセス: なし

## 利用している外部モジュールやライブラリの説明

- aws cli: AWSリソースの操作
- aws cdk: AWSリソースのプロビジョニング
- nodejs: cdk実行環境
- python: cdk、pip依存パッケージのインストール  
- その他のツール: zip、jq

## エラー処理の方法

コマンドの実行結果に応じて、カラーコードを用いてコンソールにエラーメッセージを出力し、適切にスクリプトを終了させています。
並列実行時の留意点はありません。
前提条件として、AWSクレデンシャルが適切に設定されている必要があります。

